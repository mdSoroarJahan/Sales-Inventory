<script>
    // Load dashboard summary on component initialization
    loadSummary();

    /**
     * Fetch and display dashboard summary statistics
     */
    async function loadSummary() {
        showLoader();
        try {
            let res = await axios.get('/summary');

            if (res.status === 200 && res.data['status'] === 'success') {
                const data = res.data.data;

                // Populate dashboard cards with summary data
                document.getElementById('product').innerText = data.total_products;
                document.getElementById('category').innerText = data.total_categories;
                document.getElementById('customer').innerText = data.total_customers;
                document.getElementById('invoice').innerText = data.total_invoices;
                document.getElementById('total').innerText = data.total_amount;
                document.getElementById('vat').innerText = data.total_vat;
                document.getElementById('payable').innerText = data.total_payable;
            } else {
                errorToast(res.data['message'] || "Failed to load summary");
            }
            hideLoader();
        } catch (err) {
            hideLoader();

            // Handle API error responses
            if (err.response && err.response.status === 422) {
                let errors = err.response.data.errors;
                for (let [field, messages] of Object.entries(errors)) {
                    errorToast(messages[0]);
                }
            } else if (err.response && err.response.status === 500) {
                errorToast(err.response.data.message || "Server error occurred");
            } else {
                errorToast("Something went wrong");
            }
        }
    }
</script>
